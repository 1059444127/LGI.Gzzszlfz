        public static string SubStringBetween(string source, string s1, string s2)
        {
            var idx1 = source.IndexOf(s1);
            var idx2 = source.IndexOf(s2);
            if (idx2 == -1 || idx1 == -1)
                return "";

            if (idx2 < idx1)
                return "";

            return source.Substring(idx1+s1.Length, idx2 - idx1 - s1.Length);
        }

